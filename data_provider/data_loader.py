class Dataset_AIS(Dataset):
    def __init__(self, root_path, flag='train', size=None,
                 features='M', data_path='AISData.pkl',
                 target='OT', scale=True, timeenc=0, freq='h'):

        # size [seq_len, label_len, pred_len]
        self.seq_len = size[0]
        self.label_len = size[1]
        self.pred_len = size[2]
        self.sum_points = self.seq_len + self.pred_len
        # init
        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]
        self.scale = scale
        self.features = features
        self.target = target
        self.freq = freq

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        datapath = os.path.join(self.root_path, self.data_path)

        with open(datapath, "rb") as f:
            df_original = pickle.load(f)

        # 数据预处理
        df_raw = []
        moving_threshold = 0.05  # 移动速度阈值
        for V in df_original:  # 从初始速度大于0.05的时间戳开始截断
            try:
                moving_idx = np.where(V["traj"][:, 2] > moving_threshold)[0][0]
            except:
                moving_idx = len(V["traj"]) - 1  # This track will be removed
            V["traj"] = V["traj"][moving_idx:, :]
            # 保留不包含 NaN 值的轨迹数据，长度大于阈值
            if not np.isnan(V["traj"]).any() and len(V["traj"]) > self.sum_points:
                df_raw.append(V)

        num_train = int(len(df_raw) * 0.7)
        num_test = int(len(df_raw) * 0.2)
        num_vali = len(df_raw) - num_train - num_test
        border1s = [0, num_train, len(df_raw) - num_vali]
        border2s = [num_train, num_train + num_test, len(df_raw)]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        self.data_x = df_raw[border1:border2]
        self.data_y = df_raw[border1:border2]

    def __getitem__(self, index):
        seqlen = self.sum_points  # 限制轨迹长度
        V = self.data_x[index]
        m_v = V["traj"][:seqlen, :2]  # lat, lon
        m_v[m_v > 0.9999] = 0.9999  # 异常值处理
        seq_x = m_v[:self.seq_len, :]  # 观测数据
        seq_y = m_v[self.seq_len:, :]  # 预测数据 (24,4)
        return _torch(seq_x, seq_y)

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)


